4. เลือกเส้นทางการปีน
สำหรับการปีนแบบ Top-Roping นั้น จะเริ่มด้วยหมายเลข 5 ตามด้วยจุดทศนิยมและตัวเลขที่ตามมาด้านหลังนั้นแทนระดับความยากง่ายในการปีน เช่นระดับ 5.5 , 5.6 เป็นระดับของผู้เริ่มต้น และเมื่อตัวเลขหลังจุดทศนิยมน้อยลงอย่าง 5.12 ก็แสดงถึงระดับความยากของการปีน ส่วนการปีนแบบ Bouldering จะให้เรท V โดยเริ่มต้นที่ V0 แรกที่คุณเริ่มปีนหน้าผานั้นต้องเริ่มด้วยสองมือที่จับไปที่หลุมหรือรูบนหน้าผา โดยสองขาก็ต้องพ้นจากพื้นมาเช่นเดียวกัน จากนั้นจึงค่อยๆ ปีนตามเส้นทางที่บ่งบอกเป็นสีเดียวกันบนผนัง บางเส้นทางอาจจะไม่มีหลุมให้วางเท้าเมื่อเริ่มต้น ซึ่งเริ่มง่ายๆ เพียงการยันเท้าข้างหนึ่งบนผนังเท่านั้น
 
