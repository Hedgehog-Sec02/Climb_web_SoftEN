8.	รู้จักศัพท์ในการสื่อสาร
การปีนหน้าผา สิ่งสำคัญคือการสื่อสาร โดยเฉพาะเมื่อคุณมีพาร์ทเนอร์ที่ทำการ belay ให้กับคุณอยู่เบื้องล่าง เพราะเมื่อสื่อภาษากันเข้าใจก็จะยิ่งทำให้คุณปีนได้ดี ปีนได้ไกลถึงจุดหมาย และลดความเสี่ยงต่อการปีนพลาดที่อาจเกิดขึ้นได้
 
9.	กระโดดลงจากผาอย่างปลอดภัย
